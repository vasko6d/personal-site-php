<h2> Computationally Solving Nonlinear Membranes with Plane Stress Condition </h2>
<p>This project was completed as part of my Masters curriculum for the class "261B: Computational Mechanics of Solids and Structures." Membrane theory, plane stress condition, and the neohookean constitutive laws were used in computation. The coding itself was done in MATLAB R2015a Student Version.  The final report <a href="/home/v1/pdf/hw3.pdf">is linked here</a> and source code <a href="/home/v1/zip/source_code_2.zip">is linked here</a> </p>

<p>The overall purpose of the report was to develop a nonlinear solver for deformed membranes while enforceing the plane stress condition. The developed code was tested on several geometries, some sample meshes and results are shown below.</p>
